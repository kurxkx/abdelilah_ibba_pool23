#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (i >= nb)
		return (1);
	while (i <= (nb / i))
	{
		if (i * i != nb)
			i++;
		else
			return (i);
	}
	return (0);
}
